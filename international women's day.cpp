void solve() {
    int n, k;
    cin >> n >> k;
    
    vector<int> ba9i(k, 0);
    for (int i = 0; i < n; i++) {
        int d;
        cin >> d;
        ba9i[d % k]++;
    }
    
    int pairs = 0;
    
    pairs += ba9i[0] / 2 * 2;

    for (int i = 1; i <= k / 2; i++) {
        if (i == k - i) {
            pairs += ba9i[i] / 2 * 2;
        } else {
            pairs += min(ba9i[i], ba9i[k - i]) * 2;
        }
    }

    cout << pairs << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    
        solve();

    return 0;
}



