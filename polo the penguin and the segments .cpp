#include <bits/stdc++.h>
using namespace std;
//and mazal m3rftch n7l PA M disappointed:::/ 
void solve() {
    int n, k;
    cin >> n >> k;
    
    vector<pair<int, int>> segments(n);
    int res = 0;
    
    for (int i = 0; i < n; i++) {
        int l, r;
        cin >> l >> r;
        segments[i] = {l, r};
        res += (r - l + 1);
    }
    
    int restant = res % k;
    int minn = 0;
    
    if (restant != 0) {
        minn = k - restant;
    }
    
    cout << minn << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    solve();

    return 0;
}

