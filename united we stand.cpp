#define WAH cin.tie(nullptr);
void solve(){
 int n = 0; cin >> n ; 
 vector<int> wah ; 
 wah.assign(n,0) ; 
 for(int i = 0 ; i < n ; ++i){
    cin >> wah[i] ; 
 }
 sort(wah.begin(),wah.end()) ; 

 if(wah.back() == wah[0]){
    cout << "-1\n" ; 
    return;
 }else{
    int wee = 0 ; 
    while(wah[wee] == wah[0]) wee++ ; 
    cout << wee << " " << n- wee <<"\n" ; 
    for(int j = 0 ; j < wee ; ++j) cout << wah[j] << " " ;
    for(int j = wee ; j <n ; ++j) cout << wah[j]  << " " ;

 }
}
