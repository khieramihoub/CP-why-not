void solve() {
   string wah ; cin >> wah ; 
   map<char,int> wa3 ; 
   int res = 0 ; 
   for(auto c : wah){
    wa3[c]++ ; 
    res = max(res , wa3[c]) ; 
   }
   if(res == 1 || res == 2){
    cout << 4 << endl ;
   }else if(res == 3){
    cout << 6 << endl ; 
   }else{
    cout << -1 << endl ; 
   }

}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t ; cin >> t ; 
    while(t--){
    solve();
    }
    return 0;
}

