#include<bits/stdc++.h>
using namespace std;
//code fachil 

void solve() {
    int n, m, k;
    cin >> n >> m >> k;

    int a = n / k;
    int b = min(m, a);

    m -= b;

    int b2 = (m + k - 2) / (k - 1);

    cout << b - b2 << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}

